// Arithmetic operators
// Addition +
// Subtraction -
// Division /
// Multiplication *
// Modular %
let num1 = 20;
let num2 = 10;
let num3 = 5;

// Addition of num1 and num2
let addTotal = num1 + num2;
console.log(addTotal); // Output: 30

// Subtraction of num2 from num3
let subTotal = num2 - num3;
console.log(subTotal); // Output: 5

// Division of num1 by num3
let divideTotal = num1 / num3;
console.log(divideTotal); // Output: 4

// Multiplication of 14 and 3
let multiplyTotal = 14 * 3;
console.log(multiplyTotal); // Output: 42

// Modular of 7 by 2
let modularTotal = 7 % 2;
console.log(modularTotal); // Output: 1

// Logical operators are meant for doing comparison

// > Greater than sign
console.log(num1 > num2); // Output: true

// >= Greater than sign or equal to
console.log(num2 >= num3); // Output: true

// < Less than sign
console.log(num2 < num3); // Output: false

// <= Less than sign or equal To
console.log(num2 <= num2); // Output: true

// Check if the values are exactly the same thing
console.log(num1 === num2); // Output: false

// === if values are equal
console.log(num2 === num3); // Output: false

// Check if the values are not the same thing
console.log(num1 !== num2); // Output: true

// !== if values are not equal
console.log(num2 !== num3); // Output: true
